def create_events(db: Session):
    """–°–æ–∑–¥–∞—Ç—å —Å–æ–±—ã—Ç–∏—è –≤ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö"""
    created_count = 0
    skipped_count = 0
    
    # –ù–∞—á–∏–Ω–∞–µ–º —Å –∑–∞–≤—Ç—Ä–∞—à–Ω–µ–≥–æ –¥–Ω—è –∏ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª—è–µ–º —Å–æ–±—ã—Ç–∏—è –Ω–∞ –º–µ—Å—è—Ü –≤–ø–µ—Ä–µ–¥
    base_date = datetime.now(timezone.utc) + timedelta(days=1)
    
    for idx, event_data in enumerate(EVENTS_DATA):
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ —É–∂–µ —Ç–∞–∫–æ–µ —Å–æ–±—ã—Ç–∏–µ
        existing = db.query(Event).filter(Event.title == event_data["title"]).first()
        if existing:
            skipped_count += 1
            continue
        
        # –†–∞—Å–ø—Ä–µ–¥–µ–ª—è–µ–º —Å–æ–±—ã—Ç–∏—è –ø–æ –¥–∞—Ç–∞–º (–∫–∞–∂–¥—ã–µ 2-3 –¥–Ω—è)
        days_offset = idx * 2 + random.randint(0, 1)
        event_date = base_date + timedelta(days=days_offset)
        
        # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –≤—Ä–µ–º—è –Ω–∞—á–∞–ª–∞ (–º–µ–∂–¥—É 10:00 –∏ 18:00)
        start_hour = random.randint(10, 18)
        event_date = event_date.replace(hour=start_hour, minute=0, second=0)
        
        # –í—ã—á–∏—Å–ª—è–µ–º –≤—Ä–µ–º—è –æ–∫–æ–Ω—á–∞–Ω–∏—è
        duration = event_data.get("duration_hours", 2)
        end_time = event_date + timedelta(hours=duration)
        
        # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º topics –≤ JSON
        topics_json = json.dumps(event_data.get("topics", []), ensure_ascii=False) if event_data.get("topics") else None
        
        # –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Ñ–æ—Ä–º–∞—Ç
        event_format = event_data.get("format")
        # –ï—Å–ª–∏ —Ñ–æ—Ä–º–∞—Ç –Ω–µ EventFormat, –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ç—Ä–æ–∫—É
        if isinstance(event_format, str):
            if event_format.upper() == "HYBRID":
                event_format = random.choice([EventFormat.ONLINE, EventFormat.OFFLINE])
            elif event_format.upper() == "ONLINE":
                event_format = EventFormat.ONLINE
            elif event_format.upper() == "OFFLINE":
                event_format = EventFormat.OFFLINE
            else:
                event_format = EventFormat.OFFLINE
        
        event = Event(
            title=event_data["title"],
            description=event_data["description"],
            date=event_date,
            end_time=end_time,
            event_type=event_data["event_type"],
            price=event_data.get("price"),
            format=event_format,
            location=event_data.get("location"),
            max_participants=event_data.get("max_participants", 100),
            current_participants=random.randint(0, event_data.get("max_participants", 100) // 3),  # –°–ª—É—á–∞–π–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —É–∂–µ –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö
            speaker_name=event_data.get("speaker_name"),
            speaker_bio=event_data.get("speaker_bio"),
            topics=topics_json,
        )
        
        db.add(event)
        created_count += 1
    
    db.commit()
    return created_count, skipped_count


